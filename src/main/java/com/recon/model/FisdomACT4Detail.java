package com.recon.model;

public class FisdomACT4Detail {

	private String annexure;
	private String date_of_item;
	private String items_debited;
	private String items_credited;
	private String amount;
	private String count;
	
	
	public String getAnnexure() {
		return annexure;
	}
	public void setAnnexure(String annexure) {
		this.annexure = annexure;
	}
	public String getDate_of_item() {
		return date_of_item;
	}
	public void setDate_of_item(String date_of_item) {
		this.date_of_item = date_of_item;
	}
	public String getItems_debited() {
		return items_debited;
	}
	public void setItems_debited(String items_debited) {
		this.items_debited = items_debited;
	}
	public String getItems_credited() {
		return items_credited;
	}
	public void setItems_credited(String items_credited) {
		this.items_credited = items_credited;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	
	
}
