package com.recon.model;

public class FisdomFileUploadBean {
	
	private String fileName;
	private String fileDate;
	private String createdBy;
	private String typeOfTTUM;
	private String stPath;
	private String category;
	
	
	
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getStPath() {
		return stPath;
	}
	public void setStPath(String stPath) {
		this.stPath = stPath;
	}
	public String getTypeOfTTUM() {
		return typeOfTTUM;
	}
	public void setTypeOfTTUM(String typeOfTTUM) {
		this.typeOfTTUM = typeOfTTUM;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileDate() {
		return fileDate;
	}
	public void setFileDate(String fileDate) {
		this.fileDate = fileDate;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	
	
	
	

}
